// Helper function to create a test server handler
func createTestServerHandler(t *testing.T, upgrader **Server) http.HandlerFunc {
	return http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		if *upgrader == nil {
			t.Error("Server not initialized")
			return
		}

		if strings.Contains(r.URL.Path, "/ws/write/") {
			(*upgrader).WsWriteHandler(w, r)
		} else if strings.Contains(r.URL.Path, "/ws/read/") {
			(*upgrader).WsReadHandler(w, r)
		} else {
			http.NotFound(w, r)
		}
	})
}

// Helper function to create a test client config from a test server URL
func createTestClientConfig(testServerURL string) (ClientConfig, error) {
	testURL, err := url.Parse(testServerURL)
	if err != nil {
		return ClientConfig{}, err
	}

	port, err := strconv.ParseUint(testURL.Port(), 10, 16)
	if err != nil {
		return ClientConfig{}, err
	}

	return ClientConfig{
		Addr:           testURL.Hostname(),
		Port:           uint16(port),
		Path:           "/ws/write/testroom/testtopic", // Add proper WebSocket path
		MessageType:    websocket.MessageBinary,
		ReconnectDelay: 1 * time.Second,
		PingTimeout:    10 * time.Second,
		MaxRetry:       3,
		ReadTimeout:    time.Second,
		WriteTimeout:   time.Second,
		KeepConnecting: true,
	}, nil
}

// Helper function to wait for client connection
func waitForClientConnection(client *Client, timeout time.Duration) bool {
	deadline := time.Now().Add(timeout)
	for time.Now().Before(deadline) {
		metrics := client.GetMetrics()
		if state, ok := metrics["state"]; ok && state == "Connected" {
			return true
		}
		time.Sleep(100 * time.Millisecond)
	}
	return false
}

// TestClientServerConnection tests basic client-server connection establishment
func TestClientServerConnection(t *testing.T) {
	ctx, cancel := context.WithTimeout(context.Background(), 30*time.Second)
	defer cancel()

	// Create a test server using httptest for better control
	var upgrader *Server
	testServer := httptest.NewServer(createTestServerHandler(t, &upgrader))
	defer testServer.Close()

	// Setup server
	serverConfig := DefaultServerConfig()
	serverConfig.SetDefaults()

	upgrader = NewServer(ctx, serverConfig, https.DefaultConfig())

	// Setup client
	clientConfig, err := createTestClientConfig(testServer.URL)
	if err != nil {
		t.Fatalf("Failed to create client config: %v", err)
	}

	client := NewClient(ctx, clientConfig)

	// Start connection (asynchronous)
	client.Connect()

	// Wait for connection to establish
	if !waitForClientConnection(client, 5*time.Second) {
		metrics := client.GetMetrics()
		t.Fatalf("Client failed to connect within timeout. Final state: %v", metrics["state"])
	}

	// Verify client state
	metrics := client.GetMetrics()
	if state, ok := metrics["state"]; !ok || state != "Connected" {
		t.Errorf("Expected client state to be 'Connected', got: %v", state)
	}

	// Clean up
	client.Close()
}

